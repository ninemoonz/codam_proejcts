#include "get_next_line.h"

char	*ft_strchr(const char *str, int init)
{
	int		i;
	char	*tmp_str;

	i = 0;
	tmp_str = (char *)str;
	while (str[i] != '\0')
	{
		if (str[i] == init)
			return (&tmp_str[i]);
		i++;
	}
	return (NULL);
}

char	*ft_strjoin(char const *s1, char const *s2)
{
	int		i;
	int		j;
	int		tot_len;
	char	*dest;

	tot_len = ft_strlen(s1) + ft_strlen(s2);
	dest = (char *)malloc((tot_len + 1) * sizeof(char));
	if (dest == NULL)
		return (NULL);
	i = 0;
	j = 0;
	while (s1[i])
	{
		dest[i] = s1[i];
		i++;
	}
	while (s2[j])
	{
		dest[i] = s2[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest);
}


size_t	ft_strlen(const char *str)
{
	size_t	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}